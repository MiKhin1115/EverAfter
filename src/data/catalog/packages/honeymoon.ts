const createHoneymoonPackage = (input: {
  id: string
  shopId: string
  vendorName: string
  title: string
  tagline: string
  destination: string
  country: string
  priceMMK: number
  priceMMKRange: { min: number; max: number }
  durationDays: number
  durationNights: number
  meta: HoneymoonMetaInput
  heroImage: string
  tags: string[]
}) => ({
  id: input.id,
  shopId: input.shopId,
  categoryId: 'honeymoon',
  template: 'SERVICE',
  title: input.title,
  tagline: input.tagline,
  verified: true,
  vendorName: input.vendorName,
  heroImages: [input.heroImage],
  priceMMK: input.priceMMK,
  priceMMKRange: input.priceMMKRange,
  duration: durationLabel(input.durationDays, input.durationNights),
  durationDays: input.durationDays,
  durationNights: input.durationNights,
  includes: [
    `${durationLabel(input.durationDays, input.durationNights)} accommodation package`,
    `${input.meta.hotelCategory} hotel with ${input.meta.roomType}`,
    `${input.meta.mealsIncluded} meal plan`,
    input.meta.airportTransferIncluded ? 'Airport transfer included' : 'Airport transfer available',
    input.meta.toursIncluded ? 'Couple sightseeing tours included' : 'Optional tours available',
  ],
  includesGroups: [
    {
      title: 'Trip Plan',
      items: [
        `${durationLabel(input.durationDays, input.durationNights)} itinerary`,
        `${input.destination}, ${input.country}`,
      ],
    },
    {
      title: 'Stay & Meals',
      items: [`${input.meta.hotelCategory} hotel`, input.meta.roomType, input.meta.mealsIncluded],
    },
    {
      title: 'Romance Benefits',
      items: [
        input.meta.romanticDinnerIncluded ? 'Romantic dinner included' : 'Romantic dinner optional',
        input.meta.coupleSpaIncluded ? 'Couple spa included' : 'Couple spa optional',
        input.meta.honeymoonDecorationIncluded ? 'Honeymoon room decoration included' : 'Room decoration optional',
      ],
    },
  ],
  addOnGroups: premiumAddOnGroup(input.id),
  serviceMeta: {
    destination: input.destination,
    country: input.country,
    ...input.meta,
  },
  bookingMeta,
  tags: input.tags,
  policies: {
    about: `Honeymoon package for ${input.destination}.`,
    deliverables: 'Hotel booking, itinerary and travel support are included.',
    timeline: 'Confirmation within 24-48 hours after deposit payment.',
    cancellation: 'Free cancellation within 48 hours; refund policy applies by schedule.',
    reschedule: 'One free reschedule based on supplier availability.',
  },
  reviews: [],
  extraSections: [],
})

