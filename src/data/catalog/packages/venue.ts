const createVenuePackage = (input: {
  id: string
  shopId: string
  vendorName: string
  title: string
  tagline: string
  priceMMK: number
  maxPriceMMK: number
  guestRange: string
  durationHours: number
  venueType: string
  heroImage: string
  tags: string[]
}) => ({
  id: input.id,
  shopId: input.shopId,
  categoryId: 'venue',
  template: 'SERVICE',
  title: input.title,
  tagline: input.tagline,
  verified: true,
  vendorName: input.vendorName,
  heroImages: [input.heroImage],
  priceMMK: input.priceMMK,
  priceMMKRange: { min: input.priceMMK, max: input.maxPriceMMK },
  duration: `${input.durationHours} hours`,
  guestRange: input.guestRange,
  includes: [
    `${input.guestRange} guest capacity`,
    `${input.durationHours}-hour venue booking`,
    `${input.venueType} arrangement`,
    'Standard stage and seating setup',
  ],
  includesGroups: [
    {
      title: 'Space & Capacity',
      items: [`${input.guestRange} guest capacity`, `${input.venueType} arrangement`],
    },
    {
      title: 'Duration & Access',
      items: [`${input.durationHours}-hour venue booking`, 'Event-day access management'],
    },
    {
      title: 'Setup',
      items: ['Standard stage and seating setup', 'Basic sound and lighting'],
    },
  ],
  addOnGroups: buildAddOns(input.id),
  serviceMeta: {
    serviceType: 'Venue',
    venueType: input.venueType,
    guestRange: input.guestRange,
    durationHours: input.durationHours,
  },
  bookingMeta,
  tags: input.tags,
  policies: {
    about: `${input.title} package for wedding venue rental and setup.`,
    deliverables: 'Venue space, standard setup and event coordination are included.',
    timeline: 'Booking confirmation within 24 hours after deposit payment.',
    cancellation: 'Non-refundable deposit within 7 days of event',
    reschedule: 'One reschedule subject to date availability.',
  },
  reviews: [],
  extraSections: [],
})

